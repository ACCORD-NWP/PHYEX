SUBROUTINE INTERPOLATEX (KLON, KLEV, KBLOCKS, IBLOCKS, KFLEVG, P)

IMPLICIT NONE

INTEGER, INTENT(IN) :: KLON, KLEV, KBLOCKS, IBLOCKS, KFLEVG

REAL, ALLOCATABLE, DIMENSION(:, :, :) :: P
REAL, DIMENSION(KLON, KLEV, KBLOCKS) :: Z

INTEGER :: I, J, K

INTEGER :: ILEV1A, ILEV1B
REAL :: ZWA, ZWB, ZLEV

Z = P

DEALLOCATE (P)

ALLOCATE (P(KLON, KFLEVG, KBLOCKS))

DO I = 1, KFLEVG
  ZLEV = 1. + REAL(I-1)*REAL(KLEV-1)/REAL(KFLEVG-1)
  ILEV1B = MIN (CEILING (ZLEV), KLEV)
  ILEV1A = MAX (FLOOR   (ZLEV),    1)

  IF (ILEV1A == ILEV1B) THEN
    ZWA = 1.
    ZWB = 0.
  ELSE
    ZWA = REAL(ILEV1B) - ZLEV
    ZWB = ZLEV - REAL(ILEV1A)
  ENDIF

  DO J = 1, IBLOCKS
    P(:, I, J) = ZWA * Z(:, I, J) + ZWB * Z(:, I, J)
  ENDDO
ENDDO

END SUBROUTINE

